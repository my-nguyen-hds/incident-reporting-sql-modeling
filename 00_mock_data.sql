DROP TABLE IF EXISTS core_location, core_staff, core_patient, core_person, f_action_item, f_incident CASCADE;


CREATE TABLE core_person(
  person_id INT PRIMARY KEY,
  full_name TEXT,
  dob TEXT,
  phone TEXT,
  address TEXT
