CREATE TABLE f_incident(
  incident_id INT PRIMARY KEY, 
  reported_ts TEXT, 
  event_ts TEXT, 
  patient_id INT, 
  patient_key_raw TEXT, 
  location_id INT, 
  location_raw TEXT, 
  staff_id INT, 
  reported_by_raw TEXT, 
  category TEXT, 
  severity TEXT, 
  description TEXT
);

CREATE TABLE f_action_item(
  action_id INT PRIMARY KEY, 
  incident_id INT, 
  assigned_staff_id INT, 
  assigned_to_raw TEXT, 
  due_date TEXT, 
  completed_on TEXT, 
  status TEXT, 
  note TEXT
);

